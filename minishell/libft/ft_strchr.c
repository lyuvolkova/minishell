#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	c1;

	c1 = (char)c;
	while (*s != c1)
	{
		if (*s == '\0')
			return (NULL);
		s++;
	}
	return ((char *)s);
}
